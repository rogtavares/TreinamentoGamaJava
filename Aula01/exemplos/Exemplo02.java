package exemplos;

public class Exemplo02 {
    
    public static void main(String[] args) {
        // TIPO NOME
        int numero;  // cria a variável chamada numero, que guarda um valor inteiro

        numero = 12;  // (atribuição) guarda o valor 12 dentro da variável 

        System.out.println(numero); // mostra o que tem na variável
        
        numero = 2 + 3 * 5; // substitui o conteúdo da variável pelo resultado da expressão

        System.out.println(numero);

    }

}
